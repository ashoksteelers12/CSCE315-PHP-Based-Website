<h2> Introduction </h2><hr>
<p> 
This is a PHP-based website intended to help students create a personal website or portfolio for the future. This website contains a csv parser, wikitext parser, interactive gallery, search function, and additional smaller requirements. The csv parser is to read a csv file and parse the contents into a HTML table. The wikitext parser is to read wikitext code and convert it into a HTML format. The interactive gallery is to read file data from a csv file and produce sorted images in matrix, list, or description form. The search function is to search for a keyword in a page and highlight every location it appears. 
</p>

<h2> New Skills I Learned </h2><hr>
<p>
The primary skill I had to learn to successfully complete this project was PHP, since I had no knowledge or experience of the scripting language. My knowledge and experience with HTML eased the process of learning PHP within a short period of time. I utilized a website called “W3Schools” to learn the basics of PHP. I focused on printing, creating arrays, and format, since I realized PHP included lots of HTML code within its “echo” statements. 
</p><p>
Another skill I ended up learning was efficiently creating regex expressions, which was more of a trial and error process rather than a learning process. So, the learning took place after I had finished finding the right regex expression for my program. After going through test cases in a random order, I learned to start with simple cases, and work my way to more challenging test cases. I learned of various sources to test regex expressions, which made the process easier. 
</p><p>
A smaller, but important skill I learned was creating HTML forms. Though I have experience with HTML, it doesn’t include the creation of forms. I utilized the helpful “W3Schools” website to learn form creation in HTML. A form code was included in a provided starter code, which was helpful to see how the form displays and operates on simpler terms. Both of these resources helped me create a HTML form for my search page. 
</p>

<h2> Major Challenges </h2><hr>
<p>
If I were to rate my challenges, testing my program was probably the most challenging. I used the InfinityFree hosting platform for my website, but as a free platform, the platform came with many issues. The major challenge I had with utilizing this platform was the uploading of files. I received error messages for uploading my files most of the time. The files would upload without any errors only at random times. 
</p><p>
Another challenge was that I couldn’t edit my data files I already uploaded, so I had to edit elsewhere and re-upload. As a result, I used a free online compiler to test my csv and wikitext files, since they were easy to change. Occasionally, I would try uploading the test files to InfinityFree until it worked. The third challenge was that it was required of me to utilize GitHub, so I constantly copied and pasted code, which was time consuming. Overall, my solution to my major challenges with the InfinityFree platform was to have patience, since there was not much I could do on my part.
</p><p>
Since it was my first coding experience in PHP, it would be evident that a part of my major challenges was with the PHP portion of the project. I constantly confused PHP and HTML, and thus forgot many “echo” statements and semicolons. As I coded more in PHP, I gradually overcame this hurdle. Additionally, I had trouble displaying my table for the csv parser. Specifically, it was making the header row differentiate from the rest of the rows. I first went through the HTML code for creating a table to get a better understanding of how I would go about this. I came to the conclusion that I would utilize a counter function to differentiate the header row from the other rows. 
</p><p>
My last major challenge, or the least challenging of the major ones, was the creation of regex expressions. This process remained time consuming, since I went with the trial and error approach. At first, I did not realize of an online regex tool that would assist in testing regex expressions. After I came across this tool, the regex expression creation process was much easier than before. 
</p>

<h2> Assessment </h2><hr>
<p>
Every programming assignment or project provides the programmer with a new way to approach or organize their code, since the problems and goals vary from project to project and some older approaches may not work with the project at hand. This was the case with me. I tend to always comment my code after I complete it. I attempted that on Phase 1, and tried the reverse for Phase 2. To my surprise, I had an easier time completing Phase 2, since I knew what each line needed to do and where each line needed to go. I decided to incorporate this lesson I learned into my future programming projects with the hopes of improving the clarity and efficiency of my programming process. 
</p><p>
I constantly mentioned the unimportance of web development knowledge, since I was not going in that career path. After having coded in PHP, I better understood the correlation between both the backend and frontend. As someone who focuses more on the backend, this lesson was valuable, since having general knowledge of the frontend helps me, a programmer, to better improve the backend.
</p><p> 
The enjoyable part of this project was learning another technical skill and improving my coding practices. I enjoyed this part of the project, since I know this would help me greatly in my future job or future assignments and projects. The least enjoyable part was learning skills I probably will never utilize in my life. Though I get an understanding of the frontend as a backend programmer, the technical skills obtained from the project will probably never be used by me. Additionally, the website from scratch portion of this project was not enjoyable, since companies already have websites. Going into the workforce means one will rarely create a website from scratch, but only improve the one that is already there. Despite this being less enjoyable, it is still important to know, since knowing how to make a website from scratch makes it easier to improve a pre-existing website. 
</p>

