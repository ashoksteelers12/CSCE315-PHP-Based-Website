<p>
As mentioned in the introduction, this was a PHP-based website, not one made primarily out of HTML. This project, as a result, provided insight into another method for website creation for those like me, who have only created HTML webpages. Overall, I had a good learning experience throughout this project, and hope the soft skills, if not the technical ones, I obtained help me in future projects and work. A programmer is never satisfied with how a completed project is done in my opinion, since most believe there is always room for improvement. If I were to do this project differently, I would plan better and earlier. I would write more pseudocode and comments rather than dive straight into coding or programming. In the end, I had a fun time creating a PHP-based website, and was glad to have learned many skills during the process.  
